#  Slicing tutorial
#slicing starts at index beginning number and ends at but does not include ending number

#       01234567890123
LION = "Royal Purple"

print(LION[0:6])   #Royal
print(LION[0:3])    #Roy
print(LION[6:10])   #Purp
print(LION[:6])

print(LION[6:12])
print(LION[6:])